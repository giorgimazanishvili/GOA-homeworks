    # ციკლი იმუშავებს მანამდე სანამ i ნაკლებია 41-ზე.
    # თითოეულ დატრიალებაზე დაიპრინტება საიტერაციო ცვლადი (i), რომელიც ყოველ გამეორებაზე იცვლის მნიშვნელობას.
    # i = i + 1 ეს არის საიტერაციო ცვლადის ინკრემენტაცია (გაზრდა)
