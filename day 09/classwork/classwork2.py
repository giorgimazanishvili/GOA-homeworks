# ტერმინაოლში გამომიტანე 6 True და 6 False (and და or-ის გამოყენებით)

# True (and) 2 examples
print(56>5 and 4<34)
print(6.7 > 3.4 and 7.8 < 9.7)


# True (or) 2 examples
print(45>34 or 57>23)
print(4.5<5.6 or 9.8>6.7)


# False (and) 2 examples
print(45>56 and 46>23)
print(4.5>7.8 and 4.5>6.7)


# False (or) 2 examples
print(345<234 or 435<300)
print(10.1>11.2 or 11.3>12.6)



# True (or & and) 2 examples
print(34<56 and 56<23 or 56==56 )
print(4.5<3.4 and 7.8>1.9 or 77==77)


#False (or & and) 2 examples
print(456>567 and 666<222 or 444==434)
print(4.5>5.5 and 2.3<1.1 or 1234==1134) 